void GUI_PrintOnScreen(uint16_t x, uint16_t y, uint16_t fc, uint16_t bc, uint8_t *s)
{
	unsigned char i,j;
	unsigned short k,x0;
	x0=x;

	while(*s) 
	{	
		if((*s) < 128) 
		{
			k=*s;
			if (k==13) 
			{
				x=x0;
				y+=16;
			}
			else 
			{
				if (k>32) k-=32; else k=0;
	
			    for(i=0;i<16;i++)
				for(j=0;j<8;j++) 
					{
				    	if(asc16[k*16+i]&(0x80>>j))	Gui_DrawPoint(x+j,y+i,fc);
						else 
						{
							if (fc!=bc) Gui_DrawPoint(x+j,y+i,bc);
						}
					}
				x+=8;
			}
			s++;
		}
	}
}

uint8_t line_ctl=0;//第一行

uint16_t ForeFont_Color=WHITE;
uint16_t Backgound_Color=GRAY2;

//输出一行，多的换到下面一行。循环输出。
void g_print(uint8_t* dat)
{
	uint8_t cnt=0,length=strlen((const char*)dat);
	uint8_t buffer[14];
	memset(buffer,0,sizeof(buffer));
	if(length<=14)
	{
		memset(buffer,' ',sizeof(buffer));
		for(cnt=0;cnt<length;cnt++)
			buffer[cnt]=dat[cnt];
		GUI_PrintOnScreen(5,line_ctl*15+5,ForeFont_Color,Backgound_Color,buffer);
		if(line_ctl==9)
			line_ctl=0;
		else
			line_ctl++;
	}
	else
	{
		while(length-cnt>14)
		{
			memcpy(buffer,dat+cnt,14);
			GUI_PrintOnScreen(5,line_ctl*15+5,ForeFont_Color,Backgound_Color,(uint8_t*)&"              ");
			GUI_PrintOnScreen(5,line_ctl*15+5,ForeFont_Color,Backgound_Color,buffer);
			if(line_ctl==9)
				line_ctl=0;
			else
				line_ctl++;
			memset(buffer,0,sizeof(buffer));
			cnt+=14;
		}
		memcpy(buffer,dat+cnt,length-cnt);
		uint8_t t_buffer[14];
		memset(t_buffer,' ',sizeof(t_buffer));
		for(cnt=0;cnt<length;cnt++)
			t_buffer[cnt]=buffer[cnt];
		GUI_PrintOnScreen(5,line_ctl*15+5,ForeFont_Color,Backgound_Color,t_buffer);
		if(line_ctl==9)
				line_ctl=0;
		else
			line_ctl++;
	}
}

//指定输出至某一行
void l_print(uint8_t* dat,uint8_t line)
{
	uint8_t cnt=0,length=strlen((const char*)dat);
	uint8_t buffer[14];
	memset(buffer,' ',sizeof(buffer));
	if(length<=14)
	{
		for(cnt=0;cnt<length;cnt++)
			buffer[cnt]=dat[cnt];
		GUI_PrintOnScreen(5,line*15+5,ForeFont_Color,Backgound_Color,buffer);
	}
}


